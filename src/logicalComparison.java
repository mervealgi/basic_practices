public class logicalComparison {
    public static void main(String[] args) {

        int n1 = 10;
        int n2 = 11;

        System.out.println("Result1 : " + (n1 == n2));
        System.out.println("Result1 : " + (n1 < n2));
        System.out.println("Result1 : " + (n1 > n2));
        System.out.println("Result1 : " + (n1 <= n2));
        System.out.println("Result1 : " + (n1 >= n2));
        System.out.println("Result1 : " + (n1 != n2));
    }
}
