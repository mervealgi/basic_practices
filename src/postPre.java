public class postPre {
    public static void main(String[] args) {
        int x = 5;
        int y = ++x;
        System.out.println("x: " + x); // x: 6
        System.out.println("y: " + y); // y: 6

        int a = 5;
        int b = a++;
        System.out.println("a: " + a);
        System.out.println("b: " + b);





    }
}
